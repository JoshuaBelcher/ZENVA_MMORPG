// an object used to represent a specific, created chest behind-the-scenes.
// Contains the data that will be used to create an in-game chest object (made from Chest.js class)
class ChestModel {
    constructor (x, y, gold, spawnerId) {
        this.id = `${spawnerId}-${uuid.v4()}`; // a composite ID tag, for tracking in Game Manager, composed of the ID of the spawner that created this ChestModel and a unique UUID
        this.spawnerId = spawnerId; // used to track which spawner this ChestModel belongs to
        this.x = x; // coordinates from the random location generated by the spawner
        this.y = y;
        this.gold = gold; // amount of gold contained in the chest
    }
}